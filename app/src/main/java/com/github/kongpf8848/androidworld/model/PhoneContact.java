package com.github.kongpf8848.androidworld.model;

public class PhoneContact {


    private String name;
    private String phone;

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public PhoneContact(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }
}
